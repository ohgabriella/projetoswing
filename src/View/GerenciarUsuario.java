    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tabelaUsuario.getSelectedRow();
        if (linhaSelecionada >= 0) {
            int resposta = JOptionPane.showConfirmDialog(this, "Deseja excluir o usuário?");
            if (resposta == JOptionPane.YES_OPTION) {
                Usuario u = usuarioBd.listar().get(linhaSelecionada);
                usuarioBd.remover(u);
                Atualizar();
            }
        } else {
            JOptionPane.showMessageDialog(this, "É necessário selecionar um Usuario", "Usuario", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

