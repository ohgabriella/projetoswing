    public static void Atualizar() {
        Object colunas[] = {"Id", "Username", "Senha"};

        tabela = new DefaultTableModel(colunas, 0);
        tabelaUsuario.setModel(tabela);

        List<Usuario> usuarios = usuarioBd.listar();

        for (int i = 0; i < usuarios.size(); i++) {

            Usuario u = usuarios.get(i);

            Object linhas[] = {u.getId(), u.getUsername(), u.getSenha()};
            tabela.addRow(linhas);

            tabelaUsuario.setValueAt(u.getId(), i, 0);
            tabelaUsuario.setValueAt(u.getUsername(), i, 1);
            tabelaUsuario.setValueAt(u.getSenha(), i, 2);

        }
    }

