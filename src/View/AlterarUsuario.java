    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
        String user = username.getText();
        String pass = senha.getText();

        if (!user.equals(usuario.getUsername()) || !pass.equals(usuario.getSenha())) {
            usuario.setUsername(user);
            usuario.setSenha(pass);
            //usuario.setId(linhaSelecionada);

            usuarioBd.alterar(usuario);
            Atualizar();
            JOptionPane.showMessageDialog(this, "Usuario atualizado com sucesso", "Usuario", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Nada foi modificado!", "Usuario", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        }

    }//GEN-LAST:event_okActionPerformed

